        <div class="sick-details">
            <h2>Cataracte</h2>
            <p>
                La cataracte résulte de l'opacification progressive du
                cristallin, lentille interne de l’œil enfouie derrière l'iris.
                Troublant le passage des rayons lumineux, elle entraîne une
                baisse de la vision et une sensation de voile devant les yeux.
                Très répandue passé 75 ans, seule l'opération du remplacement du
                cristallin par un implant artificiel permet de retrouver une
                vision nette.
            </p>
        </div>
        <div class="symp">
