            <h2>Décollement de la rétine</h2>
            <p>
                Le décollement de la rétine est l'urgence ophtalmologique
                absolue. Causé par le détachement de cette membrane tapissant le
                fond de l’œil, il entraîne rapidement la cécité sans prise en
                charge immédiate. Opération en urgence ou cryothérapie par le
                froid permettent encore d'éviter le pire.
            </p>
        </div>
